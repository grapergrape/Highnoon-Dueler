// Append derived gun cards to CARD_DEFINITIONS so the rest of the system
// (deck building, card rendering, getCardDef, etc.) treats guns uniformly.
for (const g of GUNS_LIST) {
  CARD_DEFINITIONS.push({
    id: g.id,
    name: g.name,
    type: "gun",
    rarity: g.rarity,
    cost: g.cost,
    effects: g.effects ?? [],
    flavorText: g.flavor,
    backstory: g.backstory ?? null,
    classId: g.classId ?? null,
    opponentOnly: !!g.opponentOnly,
    gunStats: { mag: g.mag, damage: g.damage, accuracy: g.accuracy },
  });
}

const byId = new Map(CARD_DEFINITIONS.map((c) => [c.id, c]));
const CLASS_SHOWDOWN_CATALOG_BY_CLASS = new Map();

for (const card of CARD_DEFINITIONS) {
  if (card.type !== "showdown") continue;
  if (card.opponentOnly) continue;
  if (typeof card.classId !== "string") continue;
  if (!Number.isFinite(card.showdownSlot)) continue;
  if (!CLASS_SHOWDOWN_CATALOG_BY_CLASS.has(card.classId)) {
    CLASS_SHOWDOWN_CATALOG_BY_CLASS.set(card.classId, []);
  }
  CLASS_SHOWDOWN_CATALOG_BY_CLASS.get(card.classId).push(card);
}

for (const catalog of CLASS_SHOWDOWN_CATALOG_BY_CLASS.values()) {
  catalog.sort((a, b) => a.showdownSlot - b.showdownSlot);
}

export function getClassShowdownCatalog(classId) {
  if (!classId) return [];
  const catalog = CLASS_SHOWDOWN_CATALOG_BY_CLASS.get(classId);
  return catalog ? [...catalog] : [];
}

export function getCardDef(id) {
  return byId.get(id);
}

export function effectsForCardLevel(def, level = 1) {
  if (!def) return [];
  if (def.type !== "showdown" || !Array.isArray(def.showdownLevels)) return def.effects ?? [];
  const ix = Math.min(Math.max((level || 1) - 1, 0), def.showdownLevels.length - 1);
  return def.showdownLevels[ix] ?? def.effects ?? [];
}

export function cloneCardInstance(id) {
  const d = getCardDef(id);
  if (!d) return null;
  return {
    ...d,
    showdownLevel: d.type === "showdown" ? 1 : undefined,
    uid: `${id}_${Math.random().toString(36).slice(2, 9)}`,
  };
}

export function parseEffect(token) {
  const m = token.match(/^([a-zA-Z_]+)([+-]\d+(?:\.\d+)?)?$/);
  if (!m) return { kind: token, value: true };
  const kind = m[1];
  const raw = m[2];
  if (raw == null || raw === "") return { kind, value: true };
  const value = raw.includes(".") ? parseFloat(raw) : parseInt(raw, 10);
  return { kind, value };
}

/** Returns all cards matching the given rarity. */
export function getCardsByRarity(rarity) {
  return CARD_DEFINITIONS.filter((c) => c.rarity === rarity && !c.opponentOnly);
}

/** Returns all cards of the given type. */
export function getCardsByType(type) {
  return CARD_DEFINITIONS.filter((c) => c.type === type && !c.opponentOnly);
}
